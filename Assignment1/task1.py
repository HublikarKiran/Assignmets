# Task 1: Perform Basic Mathematical Operations


num_1 = int(input("Enter 1st number :"))
num_2 = int(input("Enter 2nd number :"))
print("Addition of two numbers is :", num_1+num_2)
print("Subtraction of two numbers is :", num_1-num_2)
print("Multiplication of two numbers is :", num_1*num_2)
print("Division of two numbers is :", num_1/num_2, "Division writes quotient")
print("Modulus of two numbers is :", num_1 % num_2, "Modulus writes reminder")
print("Exponentiation of two numbers is :", num_1**num_2)
print("Floor Division of two numbers is :", num_1//num_2,
      "Floor division writes only integer part and leaves decimal part")
